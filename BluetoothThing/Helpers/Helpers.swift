import Foundation

extension Data {
    var uint8: UInt8 {
        get {
            var number: UInt8 = 0
            self.copyBytes(to:&number, count: MemoryLayout<UInt8>.size)
            return number
        }
    }
    
    var hexEncodedString: String {
        return map { String(format: "%02hhx", $0) }.joined()
    }
}
